/****************************************************
 * Esquema de conexion de la pantalla al MSP430:
 *
 * Pines de la USCIB:
 *      DIN: MOSI (P1.7)
 *      CLK: P1.5
 * Pines de control:
 *      CS:  P1.0
 *      D/C: P1.2
 * Si se quiere cambiar la configuración de los pines, se
 * deben tocar los defines a continuación
 *
 *****************************************************/




#define LCD5110_SCLK_PIN BIT7
#define LCD5110_DN_PIN BIT6
#define LCD5110_SCE_PIN BIT4
#define LCD5110_DC_PIN BIT5
#define LCD5110_SELECT P3OUT &= ~LCD5110_SCE_PIN
#define LCD5110_DESELECT P3OUT |= LCD5110_SCE_PIN
#define LCD5110_SET_COMMAND P3OUT &= ~LCD5110_DC_PIN
#define LCD5110_SET_DATA P3OUT |= LCD5110_DC_PIN
#define LCD5110_COMMAND 0
#define LCD5110_DATA 1

#define SPI_MSB_FIRST UCB0CTL0 |= UCMSB // or UCA0CTL0 |= UCMSB (USCIA) or USICTL0 &= ~USILSB (USI)
#define SPI_LSB_FIRST UCB0CTL0 &= ~UCMSB // or UCA0CTL0 &= ~UCMSB or USICTL0 |= USILSB (USI)



